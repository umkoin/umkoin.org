    <h2 id="balances">Баланси<span class="titlelight"> - ланцюжок блоків</span></h2>
    <p>Ланцюжок блоків - це <b>публічний колективний реєстр</b> на якому заснована уся мережа Умкойн. Усі підтверджені транзакції включаються до ланцюжка блоків. На основі цієї інформації умкойн-гаманці можуть розрахувати залишок вашого балансу і перевірити те, що у нових транзакціях умкоїни дійсно витрачаються їх власником. Цілісність та хронологічний порядок ланцюжка блоків засновані на надійній <a href="/uk/vocabulary.php#cryptography">криптографії</a>.</p>

    <h2 id="transactions">Транзакції<span class="titlelight"> - приватні ключі</span></h2>
    <p>Транзакція - це <b>передача коштів між умкойн-гаманцями</b>, інформація про яку включається до ланцюжка блоків. Умкойн-гаманці містять конфіденційну інформацію, що називається <a href="/uk/vocabulary.php#private-key"><i>приватний ключ</i></a> або сід, який використовується для підпису транзакцій, забезпечуючи математичний доказ того, що транзакція дійсно погоджена власником гаманця. Цей <a href="/uk/vocabulary.php#signature"><i>підпис</i></a> також запобігає зміні транзакції після того, як вона була передана до мережі. Усі транзакції транслюються між користувачами і починають підтверджуватись мережею протягом наступних 10 хвилин за допомогою процесу, що має назву <a href="/uk/vocabulary.php#mining"><i>майнінг або видобуток</i></a>.</p>

    <h2 id="processing">Обробка <span class="titlelight"> - видобуток ("майнінг")</span></h2>
    <p>Видобуток - це <b>розподілена система консенсусу</b>, що використовується для <a href="/uk/vocabulary.php#confirmation"><i>підтвердження</i></a> транзакцій, що очікують, шляхом включення їх до ланцюжка блоків. Видобуток забезпечує хронологічний порядок транзакцій у ланцюжку блоків, захищає нейтральність мережі, а також дозволяє різним комп'ютерам домовитись про єдиний стан системи. Для того, щоб транзакції були підтверджені, вони повинні бути запаковані до <a href="/uk/vocabulary.php#block"><i>блоку</i></a>, який відповідає суворим криптографічним правилам і повинен бути перевірений мережею. Ці правила не дозволяють змінювати попередні блоки, тому що це зробить недійсними усі наступні блоки. Видобуток також створює аналог конкурентної лотереї, яка не дозволяє будь-кому з легкістю послідовно додавати нові блоки до ланцюжка блоків. Таким чином, ніхто не може контролювати ланцюжок блоків чи замінювати її частини для звороту своїх витрат.</p>

