    <h2 id="basics">Основи для нового користувача</h2>
    <p>Як новий користувач, ви можете <a href="/uk/getting-started.php">почати</a> користуватися Умкойн навіть без розуміння технічних деталей. Щойно ви установите умкойн-гаманець на свій комп'ютер чи мобільний телефон, він згенерує вашу першу умкойн-адресу. У подальшому ви можете створювати стільки адрес, скільки вам буде потрібно. Ви можете повідомляти свої адреси друзям, щоб вони могли платити вам чи навпаки. Насправді, це дуже схоже на те як працює електронна пошта, за винятком того, що умкойн-адресу варто використовувати лише один раз.</p>

