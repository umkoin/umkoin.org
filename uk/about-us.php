    <h1>Про umkoin.org</h1>
    <p class="summary">Веб-сайт umkoin.org спрямований на те, щоб допомогти Умкойн у його сталому розвитку.</p>

    <h2 id="own">Кому належить umkoin.org?</h2>
    <p>Сьогодні цей сайт є незалежним проектом із відкритим кодом із учасниками з усього світу. Право останнього голосу при публікації належить співвласникам веб-сайту, але вся регулярна діяльність організована за допомогою подання публічного запиту на внесення змін та керується супроводжувачами сайту.</p>
    <p>Umkoin.org не є офіційним веб-сайтом Умкойн. Як і ніхто не володіє технологією електронної пошти, нікому не належить мережа Умкойн. Таким чином, ніхто не має права робити заяви від імені Умкойн.</p>

    <h2 id="control">Тоді ж... хто контролює Умкойн?</h2>
    <p>Умкойн контролюється умкойн-користувачами з усього світу. Розробники покращують програмне забезпечення, але вони не можуть наполягати на зміні правил умкойн-протоколу, тому що усі користувачі можуть самі обирати, яке програмне забезпечення використовувати. Для того, щоб зберегти сумісність, усім користувачам необхідно використовувати програмне забезпечення, що підпорядковується одним і тим самим правилам. Умкойн може добре працювати тільки при повному консенсусі між усіма користувачами. Тому, у всіх користувачів та розробників є міцний стимул до втілення та подальшого захисту цього консенсусу.</p>

    <h2 id="mission">Наша місія</h2>
    <ul>
      <li>Попереднє інформування користувачів з метою захисту їх від поширених помилок.</li>
      <li>Надання точного опису властивостей, потенційних способів використання та обмежень.</li>
      <li>Прозорі повідомлення про застереження та події, пов'язані з мережею Умкойн.</li>
      <li>Запрошення талановитих людей допомогти з розвитком Умкойн на багатьох рівнях.</li>
      <li>Забезпечення широкомасштабного бачення екосистеми Умкойн.</li>
      <li>Покращення доступності Умкойн у всьому світі за допомогою перекладу іншими мовами.</li>
      <li>Залишатись нейтральним інформаційним ресурсом про Умкойн.</li>
    </ul>

    <h2 id="help">Допоможіть нам</h2>
    <p>Ви можете повідомити про проблеми чи допомогти покращити umkoin.org на <a href="https://github.com/umkoin/umkoin.org#how-to-participate">GitHub</a>, шляхом відкриття нового питання для обговорення або запиту на включення (pull request) англійською мовою. При подачі запиту, будь ласка, не забувайте приділити належну увагу обговоренню запропонованих змін та їх виправленню. Будь ласка, не просіть про просування свого особистого бізнесу чи веб-сайту, окрім особливих випадків, на кшталт конференцій. Щиро дякуємо усім тим, хто витрачає свій час та зусилля на покращення umkoin.org!</p>

