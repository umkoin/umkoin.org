    <h1>Умкойн для розробників</h1>
    <p class="summary">Умкойн можна використовувати як для створення нових цікавих речей, так і для звичайних повсякденних потреб.</p>

    <h2 id="simple"><img class="titleicon" src="/img/icons/ico_simple.svg" alt="Icon" />Найпростіша з усіх платіжних систем</h2>
    <p>Окрім випадків, коли платіж повинен бути асоційований з автоматичними рахунками, отримання грошей так само просто, як і надсилання умкоїнів: через посилання чи QR-код. Ці прості дії доступні будь-якому користувачеві та можуть задовольнити потреби широкого загалу клієнтів. Коли це робиться публічно, це особливо підходить для прозорих пожертвувань та чайових.</p>

    <h2 id="api"><img class="titleicon" src="/img/icons/ico_conf.svg" alt="Icon" />Безліч сторонніх API</h2>
    <p>Існує безліч сторонніх сервісів опрацювання платежів, що надають свої API: вам необов'язково зберігати умкоїни на своєму сервері і мати справу з пов'язаними з цим питаннями безпеки. Крім того, більшість цих API дозволяють вам опрацьовувати рахунки й обмінювати ваші умкоїни на свою місцеву валюту за недорогими цінами.</p>

    <h2 id="own"><img class="titleicon" src="/img/icons/ico_own.svg" alt="Icon" />Ви можете створити свою власну фінансову систему.</h2>
    <p>Якщо ви не використовуєте будь-які сторонні API, ви можете інтегрувати вузол Umkoin безпосередньо у свої програми, що дозволяє вам стати вашим власним банком і процесором платежу. З усіма обов'язками, які випливають з цього, ви можете створювати чудові системи, які обробляють транзакції умкойн, як вам заманеться.</p>

    <h2 id="invoice"><img class="titleicon" src="/img/icons/ico_invoice.svg" alt="Icon" />Використання умкойн-адрес для відслідковування операцій</h2>
    <p>Умкойн створює унікальну адресу для кожної транзакції. Тому, якщо ви збираєтесь створити платіжну систему, пов'язану з виставленням рахунків, просто згенеруйте та відслідковуйте окрему умкойн-адресу для кожного платежу. Ніколи не варто використовувати одну й ту саму адресу для декількох транзакцій.</p>

    <h2 id="security"><img class="titleicon" src="/img/icons/ico_lock.svg" alt="Icon" />Максимум безпеки на стороні клієнта</h2>
