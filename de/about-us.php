    <h1>Über umkoin.org</h1>
    <p class="summary">Umkoin.org ist einer nachhaltigen Entwicklung von Umkoin gewidmet.</p>

    <h2 id="own">Wem gehört umkoin.org?</h2>
    <p>Umkoin.org ist der ursprünglich für die erste Umkoin-Webseite benutzte Domainname. Registriert und verwaltet wurde/wird die Website durch die <a href="/de/development.php">Umkoin Hauptentwickler</a>. Umkoin.org ist keine offizielle Webseite. Genauso wie niemand die E-Mail-Technologie besitzt, besitzt auch niemand das Umkoin Netzwerk. Und somit kann niemand als Autorität im Namen von Umkoin sprechen.</p>

    <h2 id="control">Dann... wer kontrolliert Umkoin?</h2>
    <p>Umkoin wird durch alle Umkoin-Nutzer auf der ganzen Welt kontrolliert. Entwickler verbessern die Software, aber Sie können keine Änderung der Regeln im Umkoin-Protokoll erzwingen, da alle Nutzer die freie Wahl bei der Softwarenutzung haben. Um untereinander kompatibel zu bleiben, müssen alle Nutzer Software verwenden, die die selben Regeln befolgen. Umkoin kann nur bei einem vollständigen Konsens zwischen allen Benutzern ordentlich arbeiten. Daher haben alle Benutzer und Entwickler einen starken Anreiz diesen Konsens anzunehmen und zu schützen.</p>

    <h2 id="mission">Auftrag</h2>
    <ul>
      <li>Benutzer informieren um Sie vor allgemeinen Fehlern zu schützen.</li>
      <li>Eine genaue Beschreibung der Eigenschaften von Umkoin, potentielle Einsatzmöglichkeiten und Limitierungen geben.</li>
      <li>Alarme und Ereignisse in Bezug auf das Umkoin Netzwerk anzeigen.</li>
      <li>Talentierte Menschen einladen, um der Entwicklung von Umkoin auf vielen Ebenen zu helfen.</li>
      <li>Einblick in das umfangreiche Umkoin-Ökosystem ermöglichen</li>
      <li>Die weltweite Erreichbarkeit von Umkoin durch Internationalisierung verbessern.</li>
      <li>Eine neutrale Informationsquelle über Umkoin bleiben.</li>
    </ul>

    <h2 id="help">Hilf uns</h2>
    <p>Sie können jedes Problem durch Eröffnen eines "Issues" oder "Pull Requests" in englischer Sprache auf <a href="https://github.com/umkoin/umkoin.org#how-to-participate">GitHub</a> melden. Wenn Sie ein "Pull Request" übermitteln, nehmen Sie sich bitte die nötigte Zeit, um die Änderungen zu diskutieren und ihre Arbeit anzupassen. Bitte fragen Sie nicht nach Werbung für ihr eigenes Unternehmen oder ihre Website, außer in speziellen Fällen wie Konferenzen. Vielen Dank an alle Unterstützer, die ihre Zeit für die Verbesserung von umkoin.org aufwenden!</p>

