    <h1>Einige Dinge, die Sie wissen sollten</h1>
    <p class="summarytxt">Wenn Sie Umkoin gerade erst kennenlernen, sollten Sie ein paar Dinge wissen. Umkoin lässt Sie auf eine andere Art und Weise Geld senden als bei einer Bank. Deshalb sollten Sie sich Zeit nehmen um sich zu informieren, bevor sie Umkoin für ernsthafte Transaktionen verwenden. Umkoin sollte mindestens mit der gleichen Sorgfalt behandelt werden wie Ihre normale Geldbörse.</p>

    <h2 id="secure"><img class="titleicon" src="/img/icons/ico_key.svg" alt="Icon" />Sichern Sie Ihre Wallet</h2>
    <p>Wie im richtigen Leben, müssen Sie Ihre Wallet sicher verwahren. Umkoin macht den weltweiten Transfer von Beträgen einfacher als jemals zuvor und gibt ihnen die Kontrolle über ihr Geld. So große Möglichkeiten gehen mit großen Sicherheitsbedenken einher. Richtig eingesetzt, kann Umkoin sehr hohe Sicherheitsansprüche erfüllen. Bedenken Sie immer, dass es in Ihrer Verantwortung liegt, Ihr Geld bestmöglich abzusichern. <a href="/de/secure-your-wallet.php"><b>Lesen Sie mehr über die Sicherung Ihres Wallets</b></a>.</p>

    <h2 id="volatile"><img class="titleicon" src="/img/icons/ico_market.svg" alt="Icon" />Der Umkoin-Preis ist volatil</h2>
    <p>Der Preis einer Umkoin kann innerhalb kurzer Zeit unvorhersehbar steigen oder fallen, da das Wirtschaftssystem sehr jung ist und die Umsetzung neuartig, und manchmal wegen illiquider Märkte. Deshalb ist es nicht empfohlen, Ersparnisse in Umkoin anzulegen. Umkoin sollte als risikoreiche Vermögensanlage betrachtet werden und Sie sollten niemals Geld in Umkoin anlegen, dessen Verlust Sie sich nicht leisten können. Wenn Sie Zahlungen mit Umkoin erhalten, bieten viele Dienstleister den sofortigen Umtausch in Ihre lokale Währung an.</p>

    <h2 id="irreversible"><img class="titleicon" src="/img/icons/ico_irreversible.svg" alt="Icon" />Umkoin-Zahlungen sind nicht umkehrbar</h2>
    <p>Zahlungen mit Umkoin können nicht rückgängig gemacht werden, sie können nur durch den Empfänger zurückgezahlt werden. Daher sollten Sie nur mit Personen und Organisationen handeln, die Sie kennen und denen Sie vertrauen. Der Part der Unternehmen besteht darin, die Kontrolle über die Zahlungsaufforderungen zu behalten, die sie ihren Kunden anzeigen. Aber keine Sorge, Umkoin kann Tippfehler erkennen und in der Regel können Sie kein Geld an eine ungültige Adresse senden. In der Zukunft könnten zusätzliche Dienste vorhanden sein, die mehr Wahlmöglichkeiten und Schutz für Kunden bieten.</p>

    <h2 id="anonymous"><img class="titleicon" src="/img/icons/ico_anon.svg" alt="Icon" />Umkoin ist nicht anonym</h2>
    <p>Wenn Sie Ihre Privatsphäre mit Umkoin schützen wollen, ist ein wenig Aufwand erforderlich. Alle Umkoin-Transkationen sind öffentlich und dauerhaft im Netzwerk gespeichert, was bedeutet, dass jeder den Kontostand und die Transaktionen jeder Umkoin-Adresse einsehen kann. Die Identität des Besitzers kann aber nicht mit der Umkoin-Adresse in Verbindung gebracht werden, solange bis der Besitzer Informationen im Rahmen einer Transaktion oder anderweitig preisgibt. Daher sollte man Umkoin Adressen nur einmalig verwenden. Denken Sie immer daran, dass es in ihrer Verwantwortung liegt, sich gute Praktiken anzueignen um ihre Privatsphäre schützen zu können. <a href="/de/protect-your-privacy.php"><b>Lesen Sie mehr über den Schutz ihrer Privatsphäre</b></a>.</p>

    <h2 id="instant"><img class="titleicon" src="/img/icons/ico_fast.svg" alt="Icon" />Sofort-Transaktionen sind weniger sicher</h2>
    <p>Eine Umkoin-Transaktion ist in der Regel innerhalb weniger Sekunden verteilt und in den darauf folgenden 10 Minuten beginnt die Bestätigung. Innerhalb dieser Zeit gilt die Transaktion als glaubwürdig, aber umkehrbar. Unehrliche Nutzer könnten versuchen zu schummeln. Falls Sie nicht zu lange warten wollen, sollten Sie eine kleine Transaktionsgebühr ansetzen, oder ein System verwenden, welches unsichere Transaktionen erkennt, um die Sicherheit zu erhöhen. Für größere Summen ab 1000€ empfiehlt es sich auf 6 Bestätigungen oder mehr zu warten. Jede Bestätigung verringert das Risiko einer umkehrbaren Bestätigung <i>exponentiell</i>.</p>

    <h2 id="experimental"><img class="titleicon" src="/img/icons/ico_lab.svg" alt="Icon" />Umkoin ist noch experimentell</h2>
    <p>Umkoin ist eine experimentelle neue Währung, die auf dem ursprünglichen <a href="http://bitcoin.org" target="_blank">Bitcoin</a>-Quellcode aufbaut und wird aktiv weiterentwickelt. Auch wenn es mit steigender Nutzung immer weniger experimentell wird, sollten Sie bedenken, dass Umkoin eine neue Erfindung ist, die Ideen verfolgt, die vorher nie ausprobiert wurden. Daher kann die Zukunft von niemandem vorhergesagt werden.</p>

    <h2 id="tax"><img class="titleicon" src="/img/icons/ico_invoice.svg" alt="Icon" />Steuern und Regulierung</h2>
    <p>Umkoin ist keine offizielle Währung. Allerdings sehen die meisten Gesetzgeber immer noch Einkommens-, Umsatz- und Lohnsteuer, sowie Kapitalertragssteuern auf alle Anlagen, inklusive Umkoin, vor. Es liegt in Ihrer Verantwortung sicherzustellen, dass Sie eventuell erforderliche Steuern bezahlen und andere von Ihrer Regierung und/oder Gemeinde beschlossenen rechtlichen oder regulatorischen Erlässe einhalten.</p>

