      <p>Umkoin Core ist programmiert, um zu entscheiden, welche Blockkette gültige Transaktionen enthält. Die Benutzer von Umkoin Core akzeptieren nur Transaktionen für diese Blockkette und bilden damit die Umkoin-Blockkette, die alle anderen verwenden möchten.</p>
      <p>Es sind diese Benutzer, die <b>Umkoin dezentralisiert halten</b>. Sie führen einzeln ihre eigenen Umkoin Core-Vollknoten aus, und jeder dieser Vollknoten folgt separat exakt den gleichen Regeln, um zu entscheiden, welche Blockkette gültig ist.</p>
      <p>Es gibt keine Abstimmung oder andere korrupte Prozesse: Es gibt nur eine individuelle Software, die nach identischen Regeln arbeitet - "Mathematik" - um identische Blöcke auszuwerten und zu identischen Schlussfolgerungen zu kommen, welche Blockkette gültig ist.</p>
      <p>Diese gemeinsame Vereinbarung (Konsens genannt) erlaubt es Leuten wie Ihnen, nur gültige Umkoins zu akzeptieren und <b>Umkoins Regeln</b> sogar gegen die mächtigsten Bergleute durchzusetzen.</p>
      <p>Zusätzlich zur Verbesserung der Umkoin-Dezentralisierung erhalten Umkoin-Core-Benutzer eine <a href="/en/umkoin-core/features/validation.php">bessere Sicherheit</a> für ihre Umkoins, <a href="/en/umkoin-core/features/privacy.php">Datenschutzfunktionen</a>, die in anderen Brieftaschen nicht verfügbar sind, eine Auswahl von Benutzerschnittstellen und mehrere andere leistungsstarke Funktionen.</p>

      <p>In addition to improving Umkoin’s decentralization, Umkoin Core users get <a href="/en/umkoin-core/features/validation.php">better security</a> for their umkoins, <a href="/en/umkoin-core/features/privacy.php">privacy features</a> not available in other wallets, a choice of <a href="/en/umkoin-core/features/user-interface.php">user interfaces</a> and several other powerful features.</p>

