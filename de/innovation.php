    <h1>Innovationen bei Zahlungssystemen</h1>
    <p class="summarytxt">Das Umkoin Protokoll ermöglicht nicht nur das Senden von Geld von A nach B. Es hat viele Features und eröffnet viele Möglichkeiten, die die Community gerade entdeckt. Hier finden Sie einige Technologien die gegenwärtig erforscht und in einigen Fällen bereits in echte Produkte und Dienstleistungen verwandelt werden. Die interessantesten Verwendungszwecke für Umkoin müssen vermutlich noch entdeckt werden.</p>

    <div class="tablehalf">

      <div>
        <h2 id="control"><img class="titleicon" src="/img/icons/ico_lock.svg" alt="Icon" />Vermeidung von Betrügereien</h2>
        <p>Mit Umkoin ist ein noch nie dagewesenes Maß an Sicherheit möglich. Das Netwerk bietet Nutzern Schutz gegen die am weitesten verbreiteten Betrugsmethoden wie Rückbuchungen oder ungewollte Gebühren, und Umkoins sind unmöglich zu fälschen. Nutzer können ihre Wallets verschlüsseln oder eine Sicherheitskopie jener erstellen und Hardware-Wallets könnten es in Zukunft sehr schwierig machen, dass Geld gestohlen oder verloren geht. Umkoin ist dazu entworfen, den Nutzern die komplette Kontrolle über ihr Geld zu geben.</p>
      </div>

      <div>
        <h2 id="global"><img class="titleicon" src="/img/icons/ico_international.svg" alt="Icon" />Globale Zugänglichkeit</h2>
        <p>Alle Zahlungen auf der Welt können komplett interoperable sein. Umkoin erlaubt jeder Bank, jedem Geschäft oder Individum das sichere Senden und Empfangen von Zahlungen, überall und zu jeder Zeit, mit oder ohne Bankkonto. Umkoin ist in einer großen Zahl an Ländern verfügbar, die auf Grund deren eigenen Limitierungen unerreichbar für die meisten Zahlungssysteme sind. Umkoin vergrößert den globalen Zugang zum Handelsverkehr und es kann dem internationalen Handel helfen zu florieren.</p>
      </div>

      <div>
        <h2 id="cost"><img class="titleicon" src="/img/icons/ico_lowfee.svg" alt="Icon" />Kostengünstig</h2>
        <p>Durch die Verwendung von Kryptographie sind sichere Zahlungen ohne langsame und kostenintensive Mittelsmänner möglich. Eine Umkoin-Transaktion kann sehr viel billiger als die Alternativen und in kürzerer Zeit abgewickelt sein. Das bedeutet, dass Umkoin das Potenzial hat, in der Zukunft eine gebräuchliche Methode zum Transfer von jedlicher Währung zu werden. Umkoin könnte auch eine Rolle bei der Verringerung der Armut in vielen Ländern spielen, indem es hohe Transaktionsgebühren für das Gehalt der Arbeitnehmer senkt.</p>
      </div>

      <div>
        <h2 id="donation"><img class="titleicon" src="/img/icons/ico_donate.svg" alt="Icon" />Trinkgeld und Spenden</h2>
        <p>Umkoin ist in verschiedenen Fällen eine besonders effiziente Lösung für Trinkgeld und Spenden. Eine Zahlung zu senden erfordert nur einen Mausklick, und eine Zahlung zu empfangen kann ebenso einfach sein wie das Zeigen eines QR-Codes. Spenden können für die Öffentlichkeit sichtbar gemacht werden, was eine erhöhte Transparenz für gemeinnützige Organisationen bietet. In Ernstfällen wie Naturkatastrophen könnte Umkoin zu einer schnelleren internationalen Reaktion beitragen.</p>
      </div>

      <div>
        <h2 id="crowdfunding"><img class="titleicon" src="/img/icons/ico_multi.svg" alt="Icon" />Crowdfunding</h2>
        <p>Umkoin kann für Kickstarter-artige Crowdfunding Kampagnen verwendet werden, bei denen Einzelne einem Projekt Geld zusichern, welches nur abgebucht wird, wenn genug Zusagen erhalten werden, um das Ziel zu erreichen. Solche Zusicherungs-Verträge werden vom Umkoin-Protokoll bearbeitet, was verhindert, dass eine Transaktion durchgeführt wird, bevor alle Bedingungen erfüllt sind.</p>
      </div>

      <div>
        <h2 id="micro"><img class="titleicon" src="/img/icons/ico_micro.svg" alt="Icon" />Micro-Payments</h2>
        <p>Umkoin kann Zahlungen von der Größenordnung von einem Euro und bald auch wesentlich kleineren Beträgen weiterleiten. Stellen Sie sich vor, Internetradio, pro Sekunde abgerechnet, zu hören, Websites mit einem kleinen Trinkgeld für jede nicht gezeigte Werbung zu sehen, oder Bandbreite von einem WiFi Hotspot pro Kilobyte abgerechnet, zu kaufen. Umkoin ist effizient genug um alle diese Ideen realisierbar zu machen.</p>
      </div>

      <div>
        <h2 id="mediation"><img class="titleicon" src="/img/icons/ico_balance.svg" alt="Icon" />Schlichtungsstelle</h2>
        <p>Umkoin kann benutzt werden, um innovative Streitschlichtungs-Dienstleistungen, die multipe Signaturen verwenden, zu entwickeln. Solche Dienste könnten es Dritten ermöglichen, eine Transaktion zu bestätigen oder abzulehnen im Falle einer Meinungsverschiedenheit zwischen den anderen Parteien, ohne Kontrolle über das Geld zu haben. Da diese Dienstleistungen mit jedem Nutzer und Händler, die Umkoin verwenden, kompatibel wären, würde dies wahrscheinlich zu freiem Wettbewerb und höheren Qualitätsstandarts führen.</p>
      </div>

      <div>
        <h2 id="multisig"><img class="titleicon" src="/img/icons/ico_multisig.svg" alt="Icon" />Multi-Signature Konten</h2>
        <p>Mehrfache Signaturen gestatten es, dass eine Transaktionen erst dann vom Netzwerk akzeptiert wird, wenn eine bestimmte Anzahl von einer definierten Gruppe von Personen die Transaktion signieren. Dies könnte von einem Verwaltungsrat verwendet werden, um ein einzelnes Mitglied davon abzuhalten, Teile des Vermögens ohne Einwilligung auszugeben. Dies kann auch von Banken verwendet werden, um Diebstahl zu verhindern, indem Zahlungen über einen bestimmten Limit blockiert werden, falls der Nutzer nicht zusätzliche Berechtigungsnachweise zur Verfügung stellt.</p>
      </div>

      <div>
        <h2 id="trust"><img class="titleicon" src="/img/icons/ico_law.svg" alt="Icon" />Vertrauen und Integrität</h2>
        <p>Umkoin bietet Lösungen zu vielen Vertrauensproblemen die Banken plagen. Mit selektiver Kontoführungstransparenz, digitalen Verträgen und unumkehrbaren Transaktionen kann Umkoin als Grundlage verwendet werden, um Vertrauen und Zustimmung zurück zu gewinnen. Korrupte Banken können das System nicht überlisten, um Profit durch die Kosten anderer Banken und der Öffentlichkeit zu machen. Eine Zukunft in der führende Banken Umkoin unterstützen würden, könnte helfen, Integrität und Vertrauen in Finanzinstitute wiederherzustellen.</p>
      </div>

      <div>
        <h2 id="resiliency"><img class="titleicon" src="/img/icons/ico_network.svg" alt="Icon" />Widerstandskraft und Dezentralisierung</h2>
        <p>Durch die starke Dezentralizierung hat Umkoin eine andere Art von Zahlungsnetzwerk mit einem erhöhten Maß an Stabilität und Redundanz geschaffen. Umkoin kann Millionen von Euros an Transaktionen handhaben ohne das militärischer Schutz benötigt wird. Ohne eine zentrale Schwachstelle wie ein Datenzentrum ist es ein sehr schwieriges Unterfangen das Netzwerk anzugreifen. Umkoin könnte einen interessanten Schritt vorwärts im Sichern von lokalen und globalen Finanzsystemen machen.</p>
      </div>

      <div>
        <h2 id="transparency"><img class="titleicon" src="/img/icons/ico_transparency.svg" alt="Icon" />Flexible Transparenz</h2>
        <p>Alle Umkoin-Transaktionen sind öffentlich und transparent; die Identität der Personen, die hinter einer Zahlung stehen, standardmäßig nicht. Dies erlaubt Personen und Organisationen mit flexiblen Transparenzregeln zu arbeiten. Eine Firma kann zum Beispiel manche Transaktionen und Kontostände nur für gewisse Mitarbeiter einsehbar machen, genauso wie eine gemeinnützige Organisation es öffentlich machen kann wieviel an Spenden sie täglich und monatlich erhält.</p>
      </div>

      <div>
        <h2 id="automation"><img class="titleicon" src="/img/icons/ico_solve.svg" alt="Icon" />Automatisierte Lösungen</h2>
        <p>Automatisierte Services müssen normalerweise mit den Kosten und Limitierungen von Bargeld oder Kreditkartenzahlungen umgehen. Dies beinhaltet alle möglichen Arten von Maschinen, von Busticket-Schaltern zu Kaffeemaschinen. Umkoin ist dazu geeignet, in einer neuen Generation von automatisierten Diensten verwendet zu werden, sowie deren Betriebskosten zu begrenzen. Stellen Sie sich selbstfahrende Taxis oder ein Geschäft, in dem ihr Warenkorb Sie die Einkäufe bezahlen lässt, ohne das Sie in einer Schlange anstehen müssen, vor. Viele Ideen sind möglich.</p>
      </div>

