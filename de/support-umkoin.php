    <h1>Umkoin unterstützen</h1>
    <p class="summarytxt">Das Umkoin-Protokoll wurde in einer kleinen Community geboren und wächst seitdem sehr schnell. Es gibt viele Dinge, die Sie tun können, um bei der Verbreitung und Verbesserung von Umkoin zu helfen.</p>

    <h2 id="use"><img class="titleicon" src="/img/icons/ico_multi.svg" alt="Icon" />Umkoin verwenden</h2>
    <p><a href="/de/getting-started.php">Umkoin zu verwenden</a> ist das Erste was Sie tun können, um Umkoin zu unterstützen. Es gibt vermutlich viele Fälle in denen es ihr Leben einfacher macht. Sie können Zahlungen akzeptieren und Einkäufe mit Umkoin erledigen.</p>

    <h2 id="node"><img class="titleicon" src="/img/icons/ico_network.svg" alt="Icon" />Seien Sie das Netzwerk</h2>
    <p>Falls Sie eine gute Internet Verbindung haben, dann können Sie das Umkoin Netzwerk unterstützen, indem Sie die <a href="/en/full-node.php">Full Node-Software</a> auf ihrem Computer oder Server - mit offenem Port 8333 - laufen lassen. Full Nodes sichern und leiten Transkationen weiter.</p>

    <h2 id="mining"><img class="titleicon" src="/img/icons/ico_mining.svg" alt="Icon" />Mining</h2>
    <p>Sie können damit beginnen <a href="/de/blockmining.php">Umkoins zu minen</a>, um bei der Verarbeitung von Transaktionen zu helfen. Um das Netzwerk zu schützen, sollten Sie kleineren Mining Pools beitreten, und dezentrale Pools mit Unterstützung von getblocktemplate (GBT), bevorzugen.</p>

    <h2 id="translate"><img class="titleicon" src="/img/icons/ico_translate.svg" alt="Icon" />Übersetzen</h2>
    <p>Sie können bei der Verbreitung von Umkoin mithelfen, in dem Sie in wichtigen Bereichen des Umkoin Ecosystems Übersetzungen machen oder verbessern. Suchen Sie sich einfach ein Projekt aus, bei dem Sie helfen möchten.</p>

    <h2 id="develop"><img class="titleicon" src="/img/icons/ico_conf.svg" alt="Icon" />Entwicklung</h2>
    <p>Umkoin ist quelloffen. Wenn Sie ein Entwickler sind, können Sie Ihre Superkräfte nutzen um Gutes zu tun und <a href="/de/development.php">Umkoin zu verbessern</a>. Sie können auch beeindruckende neue Dienste oder Software bauen, die Umkoin verwenden.</p>

    <h2 id="donation"><img class="titleicon" src="/img/icons/ico_donate.svg" alt="Icon" />Spenden</h2>
    <p>Der einfachste Weg zu helfen ist ein paar Umkoins zu spenden. Oder Sie können bei der Finanzierung von in der nahen Zukunft möglicherweise nützlichen Projekten helfen, die mit Umkoin im Zusammenhang stehen.</p>

    <h2 id="nonprofit"><img class="titleicon" src="/img/icons/ico_law.svg" alt="Icon" />Organisationen</h2>
    <p>Viele gemeinnützige Organisationen sind dem Schutz und der Förderung von Umkoin gewidmet. Sie können diesen Gruppen helfen in dem Sie Ihnen beitreten und an den Projekten, Diskussionen und Ereignissen teilhaben.</p>

    <h2 id="spread"><img class="titleicon" src="/img/icons/ico_spread.svg" alt="Icon" />Weitersagen</h2>
    <p>Sprechen Sie mit interessierten Menschen über Umkoin. Schreiben Sie in Ihrem Blog darüber. Sagen Sie Ihrem Lieblingsgeschäft, dass Sie gerne mit Umkoin bezahlen würden. Helfen Sie Händlerverzeichnisse aktuell zu halten. Oder seien Sie kreativ und gestalten Sie sich ein schönes Umkoin-T-Shirt.</p>

    <h2 id="wiki"><img class="titleicon" src="/img/icons/ico_wiki.svg" alt="Icon" />Dokumentation</h2>
    <p><a href="https://github.com/umkoin/umkoin.org#how-to-participate">Umkoin.org</a> bieten viele nützliche und detaillierte Informationen, und wir verbesseren die Inhalte laufend - genau wie Wikipedia. Sie können helfen, das Wiki akkurat und aktuell zu halten.</p>

