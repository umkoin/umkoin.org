    <h1>Umkoin für Entwickler</h1>
    <p class="summary">Mit Umkoin kann man Erstaunliches erschaffen oder einfach nur Grundbedürfnisse erfüllen.</p>

    <h2 id="simple"><img class="titleicon" src="/img/icons/ico_simple.svg" alt="Icon" />Das einfachste aller Zahlungssysteme</h2>
    <p>Wenn Zahlungen nicht mit mit automatischer Rechnungsstellung verbunden sein müssen, ist das Akzeptieren von Geld so einfach wie das Senden eines Umkoin: Link oder QR-Code. Diese einfache Einrichtung ist für jeden Nutzer machbar and kann die Bedürfnisse vieler Kunden erfüllen. Falls öffentlich praktiziert, ist es besonders gut geeignet für transparente Spenden und Trinkgeld.</p>

    <h2 id="api"><img class="titleicon" src="/img/icons/ico_conf.svg" alt="Icon" />Viele Third-Party-APIs</h2>
    <p>Es gibt viele Third-Party-Anbieter für Zahlungsabwicklungen, die APIs bereitstellen. Sie müssen Umkoins nicht auf Ihrem Server speichern und sich um die damit verbundenen Sicherheitsvorkehrungen kümmern. Die meisten dieser APIs ermöglichen Ihnen außerdem, Warenrechnungen abzuwickeln und Ihre Umkoins zu marktfähigen Gebühren in Ihre lokale Währung umzutauschen.</p>

    <h2 id="own"><img class="titleicon" src="/img/icons/ico_own.svg" alt="Icon" />Sie können Ihre eigene Bank sein</h2>
    <p>Wenn Sie keine Third-Party-APIs nutzen, können Sie einen Umkoin-Server direkt in Ihre Anwendungen integrieren, und dadurch Ihre eigene Bank und Zahlungsabwickler werden. Wenn Sie sich der Vielzahl der Pflichten und der Verantwortung, die dies mit sich bringt, bewusst sind, können Sie beeindruckende Systeme schaffen, die Umkoin-Transaktionen so gut wie gebührenfrei verarbeiten.</p>

    <h2 id="invoice"><img class="titleicon" src="/img/icons/ico_invoice.svg" alt="Icon" />Umkoin-Adressen zur Rechnungsprüfung</h2>
    <p>Umkoin kann so viele Umkoin-Adressen erstellen, wie Sie möchten. Wollen Sie ein Zahlungssystem entwickeln, dass mit einer Rechnung verknüpft ist, müssen Sie einfach für jede Zahlung eine Umkoin-Adresse generieren und überwachen. Umkoin Adressen sollten niemals wiederverwendet werden.</p>

    <h2 id="security"><img class="titleicon" src="/img/icons/ico_lock.svg" alt="Icon" />Die meiste Sicherheit ist auf Seite des Clients</h2>
    <p>Die Sicherheit wird zum großen Teil durch das Protokoll sichergestellt. Dies macht PCI-Konformität überflüssig und Betrugserkennung ist nur dann notwendig, wenn Dienstleistungen oder Produkte ohne Verzögerung ausgeliefert werden sollen. Das Aufbewahren Ihrer Umkoins in einer  <a href="/de/secure-your-wallet.php">sicheren Umgebung</a> und das Absichern der Zahlungsaufforderungen, die dem Benutzer angezeigt werden, sollten Ihre Hauptanliegen sein.</p>

    <h2 id="micro"><img class="titleicon" src="/img/icons/ico_micro.svg" alt="Icon" />Günstige Micro-Payments</h2>
    <p>Umkoin bietet die geringsten Zahlungsabwicklungsgebühren und kann in der Regel zum Senden von Micro-Payments in der Größenordnung von wenigen Euros verwendet werden. Umkoin ermöglicht es neue attraktive Online-Dienste zu gestalten, die vorher aufgrund vom finanziellen Einschränkungen nicht möglich waren. Dies beinhaltet verschiedene Arten von Trinkgeldsystemen und automatisierten Zahlungsverkehr.</p>

