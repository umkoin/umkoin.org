    <h1>Support Umkoin</h1>
    <p class="summarytxt">Umkoin is a protocol that was born from a small community and has grown fast since. There are a lot of things you can do to help Umkoin to spread and improve over time.</p>

    <h2 id="use"><img class="titleicon" src="/img/icons/ico_multi.svg" alt="Icon" />Using Umkoin</h2>
    <p><a href="/en/getting-started.php">Using Umkoin</a> is the first thing you can do to support Umkoin. There are probably many cases where it can make your life easier. You can accept payments and make purchases with Umkoin.</p>

    <h2 id="node"><img class="titleicon" src="/img/icons/ico_network.svg" alt="Icon" />Be the network</h2>
    <p>If you have a good Internet connection, you can strengthen the Umkoin network by keeping <a href="/en/full-node.php">full node software</a> running on your computer or server with port 6333 open. Full nodes are securing and relaying all transactions.</p>

    <h2 id="mining"><img class="titleicon" src="/img/icons/ico_mining.svg" alt="Icon" />Mining</h2>
    <p>You can start <a href="/en/blockmining.php">mining umkoins</a> to help processing transactions. In order to protect the network, you should join smaller mining pools and prefer decentralized pools with getblocktemplate (GBT) support.</p>

    <h2 id="translate"><img class="titleicon" src="/img/icons/ico_translate.svg" alt="Icon" />Translate</h2>
    <p>You can help to increase Umkoin availability by translating or improving translations inside important parts of the Umkoin ecosystem. Just pick a project you would like to help.</p>

    <h2 id="develop"><img class="titleicon" src="/img/icons/ico_conf.svg" alt="Icon" />Development</h2>
    <p>Umkoin is free software. So if you are a developer, you can use your super-powers to do good and <a href="/en/development.php">improve Umkoin</a>. Or you can build amazing new services or software that can use Umkoin.</p>

    <h2 id="donation"><img class="titleicon" src="/img/icons/ico_donate.svg" alt="Icon" />Donation</h2>
