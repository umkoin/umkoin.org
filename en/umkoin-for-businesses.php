    <h1>Umkoin for Businesses</h1>
    <p class="summary">Umkoin is a very secure and inexpensive way to handle payments.</p>

    <h2 id="lowfee"><img class="titleicon" src="/img/icons/ico_lowfee.svg" alt="Icon" />Choose your own fees</h2>
    <p>There is no fee to receive umkoins, and many wallets let you control how large a fee to pay when spending. Most wallets have reasonable default fees, and higher fees can encourage faster <a href="/en/you-need-to-know.php#instant">confirmation</a> of your transactions. Fees are unrelated to the amount transferred, so it's possible to send 100,000 umkoins for the same fee it costs to send 1 umkoin.</p>

    <h2 id="fraud"><img class="titleicon" src="/img/icons/ico_shield.svg" alt="Icon" />Protection against fraud</h2>
    <p>Any business that accepts credit cards or PayPal knows the problem of payments that are later reversed. Chargeback frauds result in limited market reach and increased prices, which in turn penalizes customers. Umkoin payments are irreversible and secure, meaning that the cost of fraud is no longer pushed onto the shoulders of the merchants.</p>

    <h2 id="international"><img class="titleicon" src="/img/icons/ico_international.svg" alt="Icon" />Fast international payments</h2>
    <p>Sending umkoins across borders is as easy as sending them across the street. There are no banks to make you wait three business days, no extra fees for making an international transfer, and no special limitations on the minimum or maximum amount you can send.</p>

    <h2 id="pci"><img class="titleicon" src="/img/icons/ico_pci.svg" alt="Icon" />No PCI compliance required</h2>
    <p>Accepting credit cards online typically requires extensive security checks in order to comply with the PCI standard. Umkoin still requires you to <a href="/en/secure-your-wallet.php">secure your wallet</a> and your payment requests. However, you do not carry the costs and responsibilities that come with processing sensitive information from your customers like credit card numbers.</p>

    <h2 id="visibility"><img class="titleicon" src="/img/icons/ico_visib.svg" alt="Icon" />Get some free visibility</h2>
    <p>Umkoin is an emerging market of new customers who are searching for ways to spend their umkoins. Accepting them is a good way to get new customers and give your business some new visibility. Accepting a new payment method has often shown to be a clever practice for online businesses.</p>

    <h2 id="multisig"><img class="titleicon" src="/img/icons/ico_multi.svg" alt="Icon" />Multi-signature</h2>
    <p>Umkoin also includes a multi-signature feature which allows umkoins to be spent only if a subset of a group of people authorize the transaction. This can be used by a board of directors to prevent any member to make expenditures without enough consent from other members, as well as to track which members allowed each payment.</p>

    <h2 id="transparency"><img class="titleicon" src="/img/icons/ico_transparency.svg" alt="Icon" />Accounting transparency</h2>
    <p>Many organizations are required to produce accounting documents about their activity. Using Umkoin allows you to offer the highest level of transparency since you can provide information your members can use to verify your balances and transactions. Non-profit organizations can also allow the public to see how much they receive in donations.</p>

