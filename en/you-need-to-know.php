    <h1>Some things you need to know</h1>
    <p class="summarytxt">If you are about to explore Umkoin, there are a few things you should know. Umkoin lets you exchange money in a different way than with usual banks. As such, you should take time to inform yourself before using Umkoin for any serious transaction. Umkoin should be treated with the same care as your regular wallet, or even more in some cases!</p>

    <h2 id="secure"><img class="titleicon" src="/img/icons/ico_key.svg" alt="Icon" />Securing your wallet</h2>
    <p>Like in real life, your wallet must be secured. Umkoin makes it possible to transfer value anywhere in a very easy way and it allows you to be in control of your money. Such great features also come with great security concerns. At the same time, Umkoin can provide very high levels of security if used correctly. Always remember that it is your responsibility to adopt good practices in order to protect your money. <a href="/en/secure-your-wallet.php"><b>Read more about securing your wallet</b></a>.</p>

    <h2 id="volatile"><img class="titleicon" src="/img/icons/ico_market.svg" alt="Icon" />Umkoin price is volatile</h2>
    <p>The price of a umkoin can unpredictably increase or decrease over a short period of time due to its young economy, novel nature, and sometimes illiquid markets. Consequently, keeping your savings with Umkoin is not recommended at this point. Umkoin should be seen like a high risk asset, and you should never store money that you cannot afford to lose with Umkoin. If you receive payments with Umkoin, many service providers can convert them to your local currency.</p>

    <h2 id="irreversible"><img class="titleicon" src="/img/icons/ico_irreversible.svg" alt="Icon" />Umkoin payments are irreversible</h2>
    <p>Any transaction issued with Umkoin cannot be reversed, they can only be refunded by the person receiving the funds. That means you should take care to do business with people and organizations you know and trust, or who have an established reputation. For their part, businesses need to keep control of the payment requests they are displaying to their customers. Umkoin can detect typos and usually won't let you send money to an invalid address by mistake. Additional services might exist in the future to provide more choice and protection for the consumer.</p>

    <h2 id="anonymous"><img class="titleicon" src="/img/icons/ico_anon.svg" alt="Icon" />Umkoin is not anonymous</h2>
    <p>Some effort is required to protect your privacy with Umkoin. All Umkoin transactions are stored publicly and permanently on the network, which means anyone can see the balance and transactions of any Umkoin address. However, the identity of the user behind an address remains unknown until information is revealed during a purchase or in other circumstances. This is one reason why Umkoin addresses should only be used once. Always remember that it is your responsibility to adopt good practices in order to protect your privacy. <a href="/en/protect-your-privacy.php"><b>Read more about protecting your privacy</b></a>.</p>

    <h2 id="instant"><img class="titleicon" src="/img/icons/ico_fast.svg" alt="Icon" />Unconfirmed transactions aren't secure</h2>
