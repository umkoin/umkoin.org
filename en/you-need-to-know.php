    <h2 id="experimental"><img class="titleicon" src="/img/icons/ico_lab.svg" alt="Icon" />Umkoin is still experimental</h2>
    <p>Umkoin is an experimental new currency built on the original <a href="http://bitcoin.org" targe="_blank">Bitcoin</a> source code, and is in active development. Each improvement makes Umkoin more appealing but also reveals new challenges as Umkoin adoption grows. During these growing pains you might encounter increased fees, slower confirmations, or even more severe issues. Be prepared for problems and consult a technical expert before making any major investments, but keep in mind that nobody can predict Umkoin's future.</p>

    <h2 id="tax"><img class="titleicon" src="/img/icons/ico_invoice.svg" alt="Icon" />Government taxes and regulations</h2>
    <p>Umkoin is not an official currency. That said, most jurisdictions still require you to pay income, sales, payroll, and capital gains taxes on anything that has value, including umkoins. It is your responsibility to ensure that you adhere to tax and other legal or regulatory mandates issued by your government and/or local municipalities.</p>

