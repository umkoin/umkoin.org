    <h1>About umkoin.org</h1>
    <p class="summary">Umkoin.org is dedicated to help Umkoin to develop in a sustainable way.</p>

    <h2 id="own">Who owns umkoin.org?</h2>
    <p>Today the site is an independent open source project with contributors from around the world. Final publication authority is held by the co-owners, but all regular activity is organized through the public <a href="https://github.com/umkoin/umkoin.org#working-with-github">pull request process</a> and managed by the site co-maintainers.</p>
    <p>Umkoin.org is not Umkoin's official website. Just like nobody owns the email technology, nobody owns the Umkoin network. As such, nobody can speak with authority in the name of Umkoin.</p>

    <h2 id="control">Then... who controls Umkoin?</h2>
    <p>Umkoin is controlled by all Umkoin users around the world. Developers are improving the software but they can't force a change in the rules of the Umkoin protocol because all users are free to choose what software they use. In order to stay compatible with each other, all users need to use software complying with the same rules. Umkoin can only work decently with a complete consensus between all users. Therefore, all users and developers have strong incentives to adopt and protect this consensus.</p>

    <h2 id="mission">Mission</h2>
    <ul>
      <li>Inform users to protect them from common mistakes.</li>
      <li>Give an accurate description of Umkoin properties, potential uses and limitations.</li>
      <li>Display transparent alerts and events regarding the Umkoin network.</li>
      <li>Invite talented humans to help with Umkoin development at many levels.</li>
      <li>Provide visibility to the large scale Umkoin ecosystem.</li>
      <li>Improve Umkoin worldwide accessibility with internationalization.</li>
      <li>Remain a neutral informative resource about Umkoin.</li>
    </ul>

    <h2 id="help">Help us</h2>
    <p>You can report any problem or help to improve umkoin.org on <a href="https://github.com/umkoin/umkoin.org#how-to-participate">GitHub</a> by opening an issue or a pull request in English. When submitting a pull request, please take required time to discuss your changes and adapt your work. Please don't ask for promotion for your personal business or website, except for special cases like conferences. Many thanks to all contributors who are spending time improving umkoin.org!</p>

