    <h1>Paying taxes on Umkoin income</h1>
    <p>Tax compliance is a topic of concern for small businesses. We aren't accountants or lawyers, and can't give legal or accounting advice.</p>
    <p>But in many respects, Umkoin transactions work very much like cash. Just like Umkoin, cash is anonymous and doesn't leave a paper trail, yet is widely used in commerce every day.</p>
    <p>Ask yourself how you would handle a cash transaction. Do you accept cash transactions? Do you normally pay taxes on cash transactions? The answer for Umkoin should probably be the same.</p>
    <p>As for how to decide what a Umkoin transaction is worth: the IRS, as far as we know, has never issued a guide mentioning how to value Umkoin transactions. But they have rules and guidelines on how to value transactions made in foreign currency or "cash equivalents". We imagine the accounting would be similar.</p>
    <p>With Umkoins, there's likely to be some difference between the value of BTC when you received them as payment, versus when you go to exchange them for another currency like USD, should you decide to do so. This scenario, likewise, would be no different if you accepted foreign currency or gold as payment. Under some scenarios, it might make sense to book the dollar value of BTC income as it is received, and then to book any difference incurred when it is exchanged for fiat currency. Under others, it might make sense to book the whole thing at the time of exchange.</p>
    <p>Perhaps you might talk to your accountant. You don't need to get into a discussion with your accountant about block chains and private keys or the philosophy behind a decentralized currency. By comparing the fundamentals of Umkoins to accounting concepts already well understood by the public, you can probably get all the answers you need. What would you ask your accountant if you decided that you wanted to accept Berkshire Bucks or 1-ounce gold coins as payment?</p>

